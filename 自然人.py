# 债的保全和担保
# 债权人应对按照约定实现债权，没有约定或约定不明的，在物保和保证（人）并存时，对债务人应先执行物保；对第三人可自由选择顺序

# 担保
# 未约定保证方式和期间
# 未约定保证方式 = 推定为连带保证，诉讼时效从债权人要求保证人程度保证责任之日起计算
# 未约定保证期间 = 从主债务履行期届满之日起算6个月
# 债务人与债权人之间重新约定，而没通知担保人的，担保人仍按原约定承担责任，超出的部分免责

# 抵押权 < 物权:抵押人的行为足以使抵押物价值减少的，抵押权人有权要求抵押人停止其行为，或提供价值相当的担保，而不能直接行使权力（非金钱给付之债不能代位）

# 以交付全部或大部分购房款的消费者 > 承包人法定优先权 > 银行抵押权

