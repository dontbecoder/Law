# 正当防卫 VS 紧急避险
# 侵害来自于"不法侵害人"    VS    危险还可来自"动物、自然灾害，等"
# 侵害正在进行，尚未结束    VS    危险已经发生或迫在眉睫，尚未消除
# 正对不正				    VS    正对正
# 行为无价值论：需要防卫意识 + 需要避险意识
# 结果无价值论：不需要防卫意识 + 不需要避险意识

