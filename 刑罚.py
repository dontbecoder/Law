# 罪刑法定原则 = 法无明文规定不为罪，法无明文规定不处罚
# 必须是成文法，不能是习惯法
# 禁止不利于行为人的事后法，但不禁止有利于行为人的事后法
# 允许扩大解释：当刑法的真实含义比字面含义宽时，扩张字面含义的解释方法
# 禁止（不利于行为人的）类推解释：对于法律没有明文规定的行为，适用有类似规定的其他条文予以处罚

# 当然解释：刑法规定虽未明示，但依形式逻辑、规范目的及事物属性的当然道理，将该事项解释为包括在该规定的适用范围之内

